            .ORG    0100H
            
            ; Set video mode
            
            LD      A,00H
            OUT     (PORTC),A

            ; Clear all

            OUT     (PORTS),A

            LD      A,WRSPR
            OUT     (PORTC),A
            XOR     A
            OUT     (PORTD),A
            LD      B,32
