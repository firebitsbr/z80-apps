            OUT     (PORTD),A
            OUT     (PORTD),A
            OUT     (PORTD),A
            OUT     (PORTD),A
            OUT     (PORTD),A
            DJNZ    L6

            ; Upload bitmaps

            LD      A,WRBMP
            OUT     (PORTC),A
            LD      A,00H
            OUT     (PORTD),A
            LD      A,00H
            OUT     (PORTD),A
            
            LD      HL,TILES
            LD      E,TILES_COUNT
            LD      C,PORTD
L2          LD      B,64
L1          OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            JP      NZ,L1
            DEC     E
            JP      NZ,L2

            ; Frame sync

            OUT     (PORTS),A

            ; Upload map

            LD      A,WRMAP
            OUT     (PORTC),A
            LD      A,00H
            OUT     (PORTD),A
            LD      A,00H
            OUT     (PORTD),A

            LD      HL,MAP
            LD      E,30
            LD      C,PORTD
L4          LD      B,40
L3          OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            OUTI
            JP      NZ,L3
            DEC     E
            JP      NZ,L4

            RET

